using System;
using System.Collections.Generic;
using System.Text;

namespace SingletonStaticInitialization
{
    public static class StaticInit
    {
       public static double Add(double a, double b)
        {
            return (a + b);
        }
        public static double Substract(double a, double b)
        {
            return (a - b);
        }
    }
}
